  SPI_InitStructure.SPI_BaudRatePrescaler = SPI_BaudRatePrescaler_32; // ???????????? SCK
  SPI_InitStructure.SPI_FirstBit = SPI_FirstBit_MSB; // ?????? ???????????? ??????? ???
  SPI_InitStructure.SPI_Mode = SPI_Mode_Master; // ????? - ??????
  SPI_Init(SPI2, &SPI_InitStructure); //??????????? SPI1
