  // ????????? ?????? NSS ?????????????? ??????????, ????????? ??? ? ???????
  // ???? ???????? ??? ? ????, ?? ??? SPI ?????? ????????, ???
  // ? ??? ??????????????? ????????? ? ??? ?????? ?????????? ???????.
